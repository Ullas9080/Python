#list store value in order 
car=["BMW","Audi","Benze"]
print(car.append("Skoda"))
print(car.pop())
print(car.reverse())
print(car)


#sets Store value in unorder
set={"BMW","Audi","Benze"}
print(set.add("I"))
print(set)

